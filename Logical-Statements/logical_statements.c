#include <stdio.h>

int main() {
    // Relational Operators
    // Relational operators compare two values 
    // and return a boolean result (true or false).
    int a = 10, b = 20;

    printf("a == b: %d\n", a == b);
    // a == b: 0
    printf("a != b: %d\n", a != b);
    // a != b: 1
    printf("a < b: %d\n", a < b);
    // a < b: 1
    printf("a > b: %d\n", a > b);
    // a > b: 0
    printf("a <= b: %d\n", a <= b);
    // a <= b: 1
    printf("a >= b: %d\n", a >= b);
    // a >= b: 0

    return 0;
}